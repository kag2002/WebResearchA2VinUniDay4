# Schema definitions matching the expected frontend schema
class ChatMessage(BaseModel):
    role: str = Field(..., description="Role of the sender: 'system', 'user', or 'assistant'")
    content: str = Field(..., description="The textual content of the message")

class ChatRequest(BaseModel):
    messages: List[ChatMessage] = Field(..., description="The conversation history")
    temperature: Optional[float] = Field(0.0, ge=0.0, le=2.0, description="Sampling temperature")
    max_tokens: Optional[int] = Field(None, gt=0, description="Maximum number of tokens to generate")

class ChatResponseUsage(BaseModel):
    prompt_tokens: int = Field(0, description="Number of tokens in the prompt")
    completion_tokens: int = Field(0, description="Number of tokens in the completion")
    total_tokens: int = Field(0, description="Total number of tokens used")

class ChatResponse(BaseModel):
    reply: str = Field(..., description="The response generated by the assistant")
    model: str = Field(..., description="The model name that generated the response")
    usage: ChatResponseUsage = Field(..., description="Token usage details")
    latency_ms: float = Field(..., description="Total processing time in milliseconds")

# Simple token counting helpers
def estimate_tokens(text: str) -> int:
    if not text:
        return 0
    # Rough fallback estimate: ~4 characters per token
    return max(1, len(text) // 4)

