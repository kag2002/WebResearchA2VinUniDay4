@app.get("/api/logs/detail")
async def log_detail(file: str):
    """Return full content of a specific log JSON file (restricted to runs/ transcripts/ samples/)."""
    target = (ROOT / file).resolve()
    # Security: only allow reading inside the project
    if not str(target).startswith(str(ROOT.resolve())):
        raise HTTPException(status_code=403, detail="Access denied")
    if not target.exists():
        raise HTTPException(status_code=404, detail="File not found")
    return json.loads(target.read_text(encoding="utf-8"))

@app.post("/api/chat", response_model=ChatResponse)
async def chat_endpoint(request: ChatRequest):
    start_time = time.time()
    
    try:
        # 1. Load prompts and tools dynamically to reflect modifications immediately
        system_prompt_path = ROOT / "artifacts" / "system_prompt.md"
        tools_path = ROOT / "artifacts" / "tools.yaml"
        
        system_prompt = system_prompt_path.read_text(encoding="utf-8")
        tool_declarations = load_tool_declarations(tools_path)
        openai_tools = to_openai_tools(tool_declarations)
        
        # 2. Build provider
        provider = make_provider(PROVIDER_NAME)
        selected_model = MODEL_NAME or getattr(provider, "default_model", None)
        
        # 3. Format history for agent
        formatted_messages = [
            {"role": msg.role, "content": msg.content} 
            for msg in request.messages
        ]
        
        # If system message is not present in formatted_messages, inject it
        has_system = any(m["role"] == "system" for m in formatted_messages)
        if not has_system:
            formatted_messages = [{"role": "system", "content": system_prompt}] + formatted_messages
            
        # 4. Execute the agent tool loop
        result = run_model_tool_loop(
            provider=provider,
            messages=formatted_messages,
            tools=openai_tools,
            model=selected_model,
            max_tool_rounds=4
        )
        
        reply = result.get("assistant_text", "")
        
        # 5. Measure latency and calculate tokens
        end_time = time.time()
        latency_ms = (end_time - start_time) * 1000
        
        # Build message history string to count tokens
        input_text = " ".join([m.content for m in request.messages])
        prompt_tokens = estimate_tokens(input_text)
        completion_tokens = estimate_tokens(reply)
        total_tokens = prompt_tokens + completion_tokens
        
        return ChatResponse(
            reply=reply,
            model=selected_model or PROVIDER_NAME,
            usage=ChatResponseUsage(
                prompt_tokens=prompt_tokens,
                completion_tokens=completion_tokens,
                total_tokens=total_tokens
            ),
            latency_ms=latency_ms
        )
        
    except Exception as e:
        import traceback
        traceback.print_exc()
        raise HTTPException(status_code=500, detail=str(e))

